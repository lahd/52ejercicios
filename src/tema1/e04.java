package tema1;

/**Hacer un pseudocodigo que imprima los numeros del 1 al 100.
 *
 * @author Luis Alberto Hernandez Dominguez
 */
public class e04 {
     public static void main(String[] args) {
        int x = 0; 
          for(int i = 0 ; i <= 100 ; i ++){ 
               x = x + i; 
          } 
          System.out. printf("El resultado es %d\n\n" , x); 
     } 
}
